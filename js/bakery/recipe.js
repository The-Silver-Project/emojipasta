const ALPHA_MAPPINGS = [
  '#### ###- -### ###- #### #### #### #--# ### -### #--# #-- #---# #---# -##- ###- -##-- ###- -### ##### #---# #---# #-----# #---# #---# #####',
  '#--# #--# #--- #--# #--- #--- #--- #--# -#- --#- #-#- #-- ##-## ##--# #--# #--# #--#- #--# #--- --#-- #---# #---# #-----# -#-#- -#-#- ---#-',
  '#### ###- #--- #--# ###- ###- #-## #### -#- --#- ##-- #-- #-#-# #-#-# #--# ###- #--#- ###- -##- --#-- #---# #---# #--#--# --#-- --#-- --#--',
  '#--# #--# #--- #--# #--- #--- #--# #--# -#- #-#- #-#- #-- #---# #--## #--# #--- #--#- #-#- ---# --#-- #---# -#-#- #-#-#-# -#-#- --#-- -#---',
  '#--# ###- -### ###- #### #--- #### #--# ### -##- #--# ### #---# #---# -##- #--- -#### #--# ###- --#-- -###- --#-- -#---#- #---# --#-- #####',
].map((row) => row.split(' '));

const NUM_MAPPINGS = [
  '### -#- ### ### #-# ### ### ### ### ###',
  '#-# ##- --# --# #-# #-- #-- --# #-# #-#',
  '#-# -#- ### ### ### ### ### --# ### ###',
  '#-# -#- #-- --# --# --# #-# --# #-# --#',
  '### ### ### ### --# ### ### --# ### ###',
].map((row) => row.split(' '));

const SPECIAL_CHARS = {
  chars: ' ~!@#.:()*+-=?|/\\^',
  mappings: [
    '- ----- # -###- --#--# - - -# #- --- --- --- --- -###- # --- --- -#-',
    '- -#--- # #--## -##### - # #- -# #-# -#- --- ### #---# # --# #-- #-#',
    '- #-#-# # #-#-# -#--#- - - #- -# -#- ### ### --- --##- # -#- -#- ---',
    '- ---#- - #--## #####- - # #- -# #-# -#- --- ### --#-- # #-- --# ---',
    '- ----- # -##-- #--#-- # - -# #- --- --- --- --- --#-- # --- --- ---',
  ].map((row) => row.split(' ')),
};

const INVALID_CHAR = [
  '---',
  '---',
  '---',
  '---',
  '###',
];

export default function getCharMapping(char, row) {
  if (!char) return '';

  const code = char.charCodeAt(0);
  // a-z
  if (code >= 97 && code <= 122) {
    const index = code - 97;
    return ALPHA_MAPPINGS[row][index];
  }

  // 0-9
  if (code >= 48 && code <= 57) {
    const index = code - 48;
    return NUM_MAPPINGS[row][index];
  }

  // Special chars
  const index = SPECIAL_CHARS.chars.indexOf(char === '\t' ? ' ' : char);
  if (index > -1) {
    return SPECIAL_CHARS.mappings[row][index];
  }

  return INVALID_CHAR[row];
}
